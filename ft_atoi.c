int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	mines;

	i = 0;
	res = 0;
	mines = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i ++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			mines *= -1;
		i ++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res += str[i] - '0';
		i ++;
	}
	return (res * mines);
}
